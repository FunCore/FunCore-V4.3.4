Movement::PacketSender::PacketSender(Unit* unit, Opcodes controlled, Opcodes self, MovementInfo* info, bool broadCastSelf, bool broadCastControlled) :
    _unit(unit), _info(info), _BroadCastSelf(broadCastSelf), _BroadCastControlled(broadCastControlled), _isControlledPacket(true)
{
    if (unit->GetTypeId() == TYPEID_PLAYER && unit->ToPlayer())
    {
        if (unit->IsSplineEnabled()) // controlled by server
            _toSend = controlled;
        else
        {
            _toSend = self;
            _isControlledPacket = false;
        }
    }
    else
    {
        _toSend = controlled;
        _BroadCastControlled = true;
    }
}

void Movement::PacketSender::Send() const
{
    WorldPacket data(_toSend);

    _info->WriteToPacket(data);

    if (((!_BroadCastSelf && !_isControlledPacket) || (!_BroadCastControlled && _isControlledPacket)) && _unit->ToPlayer())
        _unit->ToPlayer()->SendDirectMessage(&data);
    else
        _unit->SendMessageToSet(&data, (_unit->GetTypeId() == TYPEID_PLAYER) ? true : false);
}

